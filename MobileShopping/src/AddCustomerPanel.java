    private void jCustomerTableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jCustomerTableDataMouseClicked
        // TODO add your handling code here:
        int selectedRow = jCustomerTableData.getSelectedRow();
        String selectedCId = jCustomerTableData.getModel().getValueAt(selectedRow, 0).toString();
        try{
            String sql = "SELECT * FROM customer WHERE cust_id = " + selectedCId;
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            if(rs.next()){
                txtSignUpCustId.setText(rs.getString("cust_id"));
                txtSignUpUsername.setText(rs.getString("cust_name"));
                txtSignUpPassword.setText(rs.getString("cust_pass"));
                txtSignUpContactNo.setText(rs.getString("contact_no"));
                txtSignUpAddress.setText(rs.getString("cust_address"));
                txtSignUpEmailId.setText(rs.getString("email_id"));
            }
        }
        catch(Exception e){

        }
    }//GEN-LAST:event_jCustomerTableDataMouseClicked

