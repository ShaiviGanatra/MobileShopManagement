    private void fillCustomerCmb(){
        try{
            String sql = "SELECT cust_name FROM customer";
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            cmbCustName.removeAllItems();
            while(rs.next()){
                cmbCustName.addItem(rs.getString("cust_name"));
            }
        }catch(Exception e){
        }
    }
    
